public class Ex7_TypeCasting {
    public static void main(String[] args) {
        int i=126;
        double d=10.6;
        System.out.println("Original int value : "+i);
        System.out.println("Original double value : "+d);
        System.out.println("int to double : "+(double)i);
        System.out.println("double to int : "+(int)d);
    }
    
}

